"""三种投资人格定义。

每个人格 = 不同投资哲学下的 system prompt + 用户分析任务模板。
新增人格只需在 PERSONAS 字典中追加一项。
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class Persona:
    id: str          # 唯一标识，前端/API 用
    name: str        # 显示名（中文）
    icon: str        # emoji，前端展示
    tagline: str     # 一句话描述风格
    system_prompt: str
    user_template: str  # 包含 {articles_text} 和 {today} 占位符


